n = int(input())

cnt = n // 5 + n // 25 + n // 125

print(cnt)

# 💡 풀이 원리 (핵심 아이디어: 르장드르의 정리)

# 1.  꼬리 0의 정의: 꼬리 0은 숫자에 10이 곱해질 때마다 발생하며, 10 = 2 * 5 로 소인수분해됩니다.
# 2.  핵심: N!을 소인수분해했을 때, 2의 개수와 5의 개수 중 더 작은 쪽이 꼬리 0의 개수가 됩니다.
# 3.  최적화: 팩토리얼에서는 2의 개수가 5의 개수보다 항상 훨씬 많으므로, 5의 개수만 세면 됩니다.
# 4.  5의 개수 계산: N!에 포함된 5의 개수는 N을 5의 거듭제곱으로 나눈 몫들의 합과 같습니다.
# 5.  범위 처리: 문제의 최대 범위 N=500을 고려할 때, 5^4 = 625는 500을 초과하므로, N // 5, N // 25, N // 125 세 항만 계산하면 됩니다.